#!/bin/bash

echo "[`date +'%Y-%m-%d %H:%M:%S'`] Installing node modules..."

# Define error handling function
handle_error()
{
  echo "[`date +'%Y-%m-%d %H:%M:%S'`] ERROR: $1" >&2
  exit 1
}

cd `dirname $0`/.. || handle_error "Can't change directory to .."
npm install || handle_error "Can't instal node module!"
